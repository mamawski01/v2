export class HoverColor {
  constructor() {}
  static alert() {
    return `hover:bg-red-950/50 backdrop-blur-sm`;
  }
  static warn() {
    return `hover:bg-yellow-800/50 backdrop-blur-sm`;
  }
  static save() {
    return `hover:bg-green-950/50 backdrop-blur-sm`;
  }
  static to() {
    return `hover:bg-purple-950/50 backdrop-blur-sm`;
  }
  static blue() {
    return `hover:bg-blue-950/50 backdrop-blur-sm`;
  }
}
